                    <p>Hoe print je een document van je laptop uit bij de HZ?</p>

                </summary>
                <p>Je moet geld op je studentenpas zetten en dan een printer opzoeken in het gebouw. Daarna moet je jouw pasje scannen en het bestand dat je wilt printen doorsturen naar print.hz via je laptop.</p>
            </details>
            <details closed="">
                <summary>
                    <p>Hoe kun je een document scannen en naar je laptop versturen op de HZ?</p>
                </summary>
                <p>Om dit te doen heb je weer je pasje nodig en daar geld op zetten. Scan je pasje bij de printer en via het schermpje kan je het document naar je laptop toe mailen als het klaar is.</p>
            </details>
            <details closed="">
                <summary>
                    <p>Hoe koop ik iets op de HZ webshop?</p>
                </summary>
                <p>Ga naar de HZ website, log in met je account en zoek een product uit. Voeg het toe aan het winkelmandje en je kunt betalen.</p>
            </details>
            <details closed="">
                <summary>
                    <p>Waar kan ik de laatste informatie en instructies vinden over wat je moet doen als je ziek bent en COVID?</p>
                </summary>
                <p>Ga naar de HZ website en zoek het tabje 'actualiteiten'. Daar vind je een pagina die over het coronavirus en de laatste updates gaat.</p>
            </details>
            <details closed="">
                <summary>
                    <p>Hoe kan je een projectruimte boeken op de HZ?</p>
                </summary>
                <p>Ga naar de HZ website, log in met je account en zoek de 'services' pagina. Klik dan op 'Helpdesk & dienst automatisering. Zo word je naar een andere pagina gestuurd waar je op 'Maak een reservering' kunt klikken en zo een ruimte kan reserveren.</p>
            </details>
            <details closed="">
                <summary>
                    <p>Wat zijn de instructies over het parkeren op de HZ parkeerplaats?</p>
                </summary>
                <p>Ga naar de HZ website en klik op 'Over de HZ'. klik dan op locaties en de locatie van de school. Op de nieuwe geopende pagina vind je de informatie over het parkeren.</p>
